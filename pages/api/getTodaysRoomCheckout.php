<?php
    $servername = "localhost";
    $username = "root";
    $password = "";
    $db = "hms";

    // Create connection
    $conn = mysqli_connect($servername, $username, $password,$db);

    $select = "SELECT * from roomDetails where DATE_FORMAT(checkoutDate, '%Y-%m-%d') = DATE_FORMAT(curdate(), '%Y-%m-%d') order by roomNo";
    $result = mysqli_query($conn, $select);

    $rows = array();
    while($row = mysqli_fetch_assoc($result)) {
        $rows[] = $row;
    }

    echo json_encode($rows);
?>